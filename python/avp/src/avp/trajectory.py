"""avp.trajectory — Pydantic types for the AVP Trajectory Spec.

Scoped re-exports of the trajectory surface (CloudEvents envelope + event
types + per-event `data` payloads + `RunStateSnapshot` + helpers). This
module mirrors the [Trajectory spec](../../../../spec/v0.1/trajectory.md).

Consumers wanting only the event stream can:

    from avp.trajectory import (
        AgentStartedEvent,
        ModelTurnEndedEvent,
        RunStateSnapshot,
        parse_event,
    )

…without dragging in Commission / Descriptor / Resolver API types they
don't use.

Source of truth for the Pydantic classes themselves is still
`avp.types` — this module is a scoped projection over that single file,
not a duplicated definition. Identity is preserved
(`isinstance(ev, AgentStartedEvent)` works regardless of which module
you imported `AgentStartedEvent` from).
"""

