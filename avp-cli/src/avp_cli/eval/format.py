"""The portable eval artifact: the typed shape of `<name>.eval.json`.

Source of truth for the eval format. `eval.schema.json` (next to the wire-spec
schemas, generated by `avp/scripts/generate-schemas.py` / `make schemas`) is
derived from these models, and `avp_cli.config` validates every config against
`EvalConfig` before building the engine, so a typo'd key or a wrong shape is a
load-time error with a field path, not a silently-ignored setting. The cloud
consumes the same schema at upload: this file IS the migration contract,
together with the prose rules in EVAL-FORMAT.md (templating, commission
references, run stamping).

Models are strict (`extra="forbid"`): the format has no vendor extension
surface, and unknown keys are far more often typos than intent.
"""

from __future__ import annotations

from typing import Any, Literal

from pydantic import BaseModel, ConfigDict, Field

_STRICT = ConfigDict(extra="forbid")

EVAL_VERSION = "0.1"


class InlineItem(BaseModel):
    """One dataset case, carried in the config itself."""

    model_config = _STRICT
    id: str | int | None = None
    prompt: str
    expected: Any = None


class InlineDataset(BaseModel):
    """Cases carried in the config: the zero-dependency source."""

    model_config = _STRICT
    source: Literal["inline"]
    items: list[InlineItem] = Field(min_length=1)


class FileDataset(BaseModel):
    """Rows from a local `.json` / `.jsonl` file. Either the rows already have
    the `{id, prompt, expected}` shape (omit `input`), or `input` is a template
    over row fields (EVAL-FORMAT.md §3; strict: unknown field names error) with
    `expected_field` / `id_field` naming the mapped columns."""

    model_config = _STRICT
    source: Literal["file"]
    path: str
    input: str | None = None
    expected_field: str | None = None
    id_field: str | None = None
    # Reduce a raw `expected_field` value to the gold answer. `expected_pattern`
    # is a regex searched against it (capture group 1, else the whole match;
    # numeric results coerce to int/float). `expected_key` wraps the result as
    # `{expected_key: value}`, the dict shape structural-match scores against.
    expected_pattern: str | None = None
    expected_key: str | None = None


class HuggingFaceDataset(BaseModel):
    """Rows from a Hugging Face dataset (needs the `huggingface` extra)."""

    model_config = _STRICT
    source: Literal["huggingface"]
    id: str
    split: str
    input: str
    config: str | None = None  # dataset config/subset (e.g. gsm8k's "main")
    expected_field: str | None = None
    id_field: str | None = None
    # See FileDataset: reduce the raw `expected_field` value to the gold answer
    # (regex capture) and optionally wrap it as a dict for structural-match.
    expected_pattern: str | None = None
    expected_key: str | None = None


class ScorerSpec(BaseModel):
    """A built-in scorer chosen by name. `threshold` applies to
    structural-match / structural-fidelity; `model` / `template` to llm-judge
    (the template renders strict over question / response / correct_answer)."""

    model_config = _STRICT
    name: Literal["exact-match", "structural-match", "structural-fidelity", "llm-judge"]
    threshold: float | None = None
    model: str | None = None
    template: str | None = None


class EvalConfig(BaseModel):
    """One eval: a dataset, a scorer, and the commissions to compare.

    `commissions` is a flat list of library ids (every commission runs on
    every agent), or a per-agent map whose keys are each agent's self-declared
    `descriptor.agent_name` (the identity that also keys a Commission's
    `enabled_builtin_*` / `agent_versions` maps). `agents` entries are
    locators: a registry name or a path to an agent's `avp-conformance.json`.
    See EVAL-FORMAT.md §2 for the identity/locator split and §4 for what the
    runner stamps onto each commission per run.
    """

    model_config = _STRICT
    eval_version: Literal["0.1"] = EVAL_VERSION
    name: str | None = None
    agents: list[str] | None = None
    dataset: InlineDataset | FileDataset | HuggingFaceDataset = Field(discriminator="source")
    scorer: ScorerSpec
    commissions: list[str] | dict[str, list[str]] = Field(min_length=1)
